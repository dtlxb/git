public class SocialLiveDataAnchor {

    private String anchor_introduction_img;
    private String anchor_introduction;
    private String anchor_name;
    private String organization;
    private String anchor_position;
    private String face_url;

    public String getAnchor_introduction_img() {
        return anchor_introduction_img;
    }

    public void setAnchor_introduction_img(String anchor_introduction_img) {
        this.anchor_introduction_img = anchor_introduction_img;
    }

    public String getAnchor_introduction() {
        return anchor_introduction;
    }

    public void setAnchor_introduction(String anchor_introduction) {
        this.anchor_introduction = anchor_introduction;
    }

    public String getAnchor_name() {
        return anchor_name;
    }

    public void setAnchor_name(String anchor_name) {
        this.anchor_name = anchor_name;
    }

    public String getOrganization() {
        return organization;
    }

    public void setOrganization(String organization) {
        this.organization = organization;
    }

    public String getAnchor_position() {
        return anchor_position;
    }

    public void setAnchor_position(String anchor_position) {
        this.anchor_position = anchor_position;
    }

    public String getFace_url() {
        return face_url;
    }

    public void setFace_url(String face_url) {
        this.face_url = face_url;
    }
}
