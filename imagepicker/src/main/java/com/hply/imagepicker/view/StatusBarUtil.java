    /**
     * 设置状态栏字体图标为黑色与否
     */
    public StatusBarUtil setStatusBarFontDark( boolean dark) {
        // 小米MIUI
        if (isMiUIV6()) {
            try {
                Window window = activity.getWindow();
                Class clazz = activity.getWindow().getClass();
                Class layoutParams = Class.forName("android.view.MiuiWindowManager$LayoutParams");
                Field field = layoutParams.getField("EXTRA_FLAG_STATUS_BAR_DARK_MODE");
                int darkModeFlag = field.getInt(layoutParams);
                Method extraFlagField = clazz.getMethod("setExtraFlags", int.class, int.class);
                if (dark) {    //状态栏亮色且黑色字体
                    extraFlagField.invoke(window, darkModeFlag, darkModeFlag);
                } else {       //清除黑色字体
                    extraFlagField.invoke(window, 0, darkModeFlag);
                }
            } catch (Exception e) {
                e.printStackTrace();
            }
        }

        // 魅族FlymeUI
        if (isFlyme()) {
            try {
                Window window = activity.getWindow();
                WindowManager.LayoutParams lp = window.getAttributes();
                Field darkFlag = WindowManager.LayoutParams.class.getDeclaredField("MEIZU_FLAG_DARK_STATUS_BAR_ICON");
                Field meizuFlags = WindowManager.LayoutParams.class.getDeclaredField("meizuFlags");
                darkFlag.setAccessible(true);
                meizuFlags.setAccessible(true);
                int bit = darkFlag.getInt(null);
                int value = meizuFlags.getInt(lp);
                if (dark) {
                    value |= bit;
                } else {
                    value &= ~bit;
                }
                meizuFlags.setInt(lp, value);
                window.setAttributes(lp);
            } catch (Exception e) {
                e.printStackTrace();
            }
        }

        // android6.0+系统
        if (Build.VERSION.SDK_INT >= Build.VERSION_CODES.M) {
            if (dark) {
                activity.getWindow().getDecorView().setSystemUiVisibility(
                        View.SYSTEM_UI_FLAG_LAYOUT_FULLSCREEN
                                | View.SYSTEM_UI_FLAG_LIGHT_STATUS_BAR);
            }
        }

        return this;
    }

    public boolean isOperableDevice() {
        return isFlyme() || isMiUIV6() || Build.VERSION.SDK_INT >= 23;
    }

    public void initForGogoal( boolean fullScreemImmersive) {
        if (!fullScreemImmersive) {
            if (isOperableDevice()) {
                setStatusBarFontDark(true);
                setColor(ContextCompat.getColor(activity, R.color.colorPrimaryDark));
            } else {
                setColor(Color.BLACK);
            }
        } else {
            setTranslucent();
        }
    }

    /**
     * 是否是小米miui6及更高版本
     */
    private static final String KEY_MIUI_VERSION_CODE = "ro.miui.ui.version.code";
    private static final String KEY_MIUI_VERSION_NAME = "ro.miui.ui.version.name";
    private static final String KEY_MIUI_INTERNAL_STORAGE = "ro.miui.internal.storage";

    public boolean isMiUIV6() {
        SharedPreferences sharedPreferences = activity.getSharedPreferences("cache_miui", Context.MODE_PRIVATE);
        //获取缓存状态
        if (sharedPreferences.getString("isMiui", "").equals("miui")) {
            return true;
        }

        Properties prop = new Properties();
        try {
            prop.load(new FileInputStream(new File(Environment.getRootDirectory(), "build.prop")));
        } catch (IOException e) {
            e.printStackTrace();
            return false;
        }
        if (prop.getProperty(KEY_MIUI_VERSION_CODE, null) != null
                || prop.getProperty(KEY_MIUI_VERSION_NAME, null) != null
                || prop.getProperty(KEY_MIUI_INTERNAL_STORAGE, null) != null) {
            SharedPreferences.Editor editor = sharedPreferences.edit();//获取编辑器
            editor.putString("isMiui", "miui");
            editor.apply();//提交修改
            return true;
        }
        ;
        return false;

    }

    /**
     * 是否是Flyme4及更高版本
     */
    private boolean isFlyme() {
        try {
            final Method method = Build.class.getMethod("hasSmartBar");
            return method != null;
        } catch (final Exception e) {
            return false;
        }
    }

